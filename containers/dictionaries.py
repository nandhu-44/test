# Dictionary operations

my_dict = {
    "name": "John",
    "age": 36,
    "country": "Norway"
}
print(f"Original dictionary \t\b\b\b\b: {my_dict}")

# Add to dictionary
my_dict["city"] = "Oslo"
print(f"Updated dictionary \t\b\b\b\b: {my_dict}")

# Modify dictionary
my_dict.update({"city": "Bergen"})
print(f"Modified dictionary \t\b\b\b\b: {my_dict}")